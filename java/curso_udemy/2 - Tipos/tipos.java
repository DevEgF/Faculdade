class main {    
    public static void main(String[] args) {
        byte numeroByte = 10; // Tipo byte
        short numeroCurto = 5; // Tipo curto
        long numeroLongo = 100000L; // Tipo longo
        int numeroInteiro = 10; // Tipo inteiro
        float numeroFlutuante = 5.75f; // Tipo ponto flutuante
        double numeroDecimal = 5.99; // Tipo ponto flutuante
        char caractere = 'A'; // Tipo caractere
        boolean verdadeiroOuFalso = true; // Tipo booleano
        String texto = "Olá, Mundo!"; // Tipo String (não é primitivo, mas muito usado)

        System.out.println("Número Byte: " + numeroByte);
        System.out.println("Número Curto: " + numeroCurto);
        System.out.println("Número Longo: " + numeroLongo);
        System.out.println("Número Inteiro: " + numeroInteiro);
        System.out.println("Número Flutuante: " + numeroFlutuante);
        System.out.println("Número Decimal: " + numeroDecimal);
        System.out.println("Caractere: " + caractere);
        System.out.println("Booleano: " + verdadeiroOuFalso);
        System.out.println("Texto: " + texto);
    }
}